package io.mlu.mde;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class IoTDeviceBeanConfig {
    @Bean(name="FridgeBean", initMethod = "initBean", destroyMethod = "destroyBean")
    public IoTDevice pintoBean() {
        IoTDevice d = new IoTDevice();
        d.setDevice("Fridge");
        d.setDescription("Temperature gauge for the freezer");
        return d;
    }

    @Bean(name="DishwasherBean", initMethod = "initBean", destroyMethod = "destroyBean")
    public IoTDevice pigeonBean() {
        IoTDevice d = new IoTDevice();
        d.setDevice("Dishwasher");
        d.setDescription("Thrashing water around");
        return d;
    }
}
